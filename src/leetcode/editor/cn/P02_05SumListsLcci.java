/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode addTwoNumbers(ListNode l1, ListNode l2) {
        ListNode pos=l1,pos2=l2,pre=null;
        int flag=0,num;
        if(l1==null)
            return l2;
        if(l2==null)
            return l1;
        while(pos!=null) {
            num = 0;
            if (pos2 != null)
                num += pos2.val;
            num += pos.val + flag;
            flag = num / 10;
            pos.val = num % 10;
            if (pos.next == null && pos2 != null){
                pos.next = pos2.next;
                pos2= null;
            }
            pre= pos;
            pos= pos.next;
            if(pos2!=null)
                pos2= pos2.next;
        }
        if(flag>0){
            pre.next= new ListNode(1);
        }
        return l1;
    }
}
